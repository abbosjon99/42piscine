#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	while (*str)
	{
		ft_putchar(*str);
		str++;
	}
}

int	main(int argc, char *argv[])
{
	int	temp;

	temp = 1;
	while (temp < argc)
	{
		ft_putstr(argv[temp]);
		ft_putstr("\n");
		temp++;
	}
	return (0);
}
